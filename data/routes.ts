export type RouteImage = {
  src: string;
  alt: string;
  width: number;
  height: number;
};

export type RouteItem = {
  /** Card heading shown to the user */
  title: string;
  /** Exact value sent to the form/API as routeName */
  routeName: string;
  description: string;
  keyPoints: string[];
  mood: string;
  forWho: string;
  /**
   * Real photo (processed WebP). `null` means the raster image is missing
   * and the card falls back to the approved SVG visual.
   */
  image: RouteImage | null;
  /** SVG fallback shown only when image is null */
  visual?: "drawbridge";
  /** Marks the navigation-zones map card (object-contain + lightbox) */
  isMap?: boolean;
};

